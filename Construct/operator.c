#include <stdio.h>
int main(){
    //& | ^ ~ << >>

    int a = 4;//00000000 00000000 00000000 00000100
    int b = 7;//00000000 00000000 00000000 00000111
              //00000000 00000000 00000000 00000100
    int c = a&b;
    printf("c = %d\n",c);
    
    int a2 = 9;//00000000 00000000 00000000 00001001
    int b2 = 5;//00000000 00000000 00000000 00000101
               //00000000 00000000 00000000 00001101
    int c2 = a2|b2;
    printf("c2 = %d\n", c2);

    int a3 = 9;//0000 1001
    int b3 = 5;//0000 0101
               //0000 1100
    int c3 = a3^b3;
    // a3 = a3^0xFF;  //reverse by bit
    printf("c3 = %d\n", c3);
    a3 = a3^b3;//exchange the value between a3 and b3;
    b3 = b3^a3;
    a3 = a3^b3;
    printf("a3 = %d, b3 = %d\n", a3, b3);   
    
    printf("d = %d\n", ~(10));//reverse ~10==-11, ~5==-6 

    int a4 = 3;//00000000 00000000 00000000 00000011;
    a4 = a4<<4;//00000000 00000000 00000000 00110000;
    printf("a4 = %d\n",a4);//a4 = a4 *2^4 == 48;
    return 0;
}
